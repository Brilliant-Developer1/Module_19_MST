class Edge
{
public:
    int a, b, w;
    Edge(int a, int b, int w)
    {
        this->a = a;
        this->b = b;
        this->w = w;
    }
};
class cmp
{
public:
    bool operator()(Edge a, Edge b)
    {
        return a.w > b.w;
    }
};

void prims(int s)
{
    priority_queue<Edge, vector<Edge>, cmp> pq;
    pq.push(Edge(1, 2, 10));
    pq.push(Edge(1, 5, 5));

    while (!pq.empty())
    {
        Edge p = pq.top();
        pq.pop();
        cout << p.w << endl;
    }
}

